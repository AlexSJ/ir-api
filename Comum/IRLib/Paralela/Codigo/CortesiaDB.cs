        public override void Ler(int id)
        {

            try
            {

                string sql = "SELECT * FROM tCortesia WHERE ID = " + id;
                bd.Consulta(sql);

                if (bd.Consulta().Read())
                {
                    this.Control.ID = id;
                    this.Nome.ValorBD = bd.LerString("Nome");
                    this.LocalID.ValorBD = bd.LerInt("LocalID").ToString();

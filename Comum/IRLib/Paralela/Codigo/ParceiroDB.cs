                this.Control.ID = ++id;
                this.Control.Versao = 0;

                sql = new StringBuilder();
                sql.Append("INSERT INTO tParceiro(ID, Parceiro, Tipo, Url) ");
                sql.Append("VALUES (@ID,'@001',@002,'@003')");

